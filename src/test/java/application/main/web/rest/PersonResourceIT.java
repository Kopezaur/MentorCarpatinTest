    @Test
    @Transactional
    public void updatePerson() throws Exception {
        // Initialize the database
        personService.save(person);
        // As the test used the service layer, reset the Elasticsearch mock repository
        reset(mockPersonSearchRepository);

        int databaseSizeBeforeUpdate = personRepository.findAll().size();

        // Update the person
        Person updatedPerson = personRepository.findById(person.getId()).get();
        // Disconnect from session so that the updates on updatedPerson are not directly saved in db
        em.detach(updatedPerson);
        updatedPerson
            .firstName(UPDATED_FIRST_NAME)
            .lastName(UPDATED_LAST_NAME)
            .email(UPDATED_EMAIL)
            .phoneNumber(UPDATED_PHONE_NUMBER)
            .birthDate(UPDATED_BIRTH_DATE)
            .weight(UPDATED_WEIGHT)
            .height(UPDATED_HEIGHT)
            .runningCoefficient(UPDATED_RUNNING_COEFFICIENT)
            .performanceCoefficient(UPDATED_PERFORMANCE_COEFFICIENT);

        restPersonMockMvc.perform(put("/api/people")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(updatedPerson)))
            .andExpect(status().isOk());

        // Validate the Person in the database
        List<Person> personList = personRepository.findAll();
        assertThat(personList).hasSize(databaseSizeBeforeUpdate);
        Person testPerson = personList.get(personList.size() - 1);
        assertThat(testPerson.getFirstName()).isEqualTo(UPDATED_FIRST_NAME);
        assertThat(testPerson.getLastName()).isEqualTo(UPDATED_LAST_NAME);
        assertThat(testPerson.getEmail()).isEqualTo(UPDATED_EMAIL);
        assertThat(testPerson.getPhoneNumber()).isEqualTo(UPDATED_PHONE_NUMBER);
        assertThat(testPerson.getBirthDate()).isEqualTo(UPDATED_BIRTH_DATE);
        assertThat(testPerson.getWeight()).isEqualTo(UPDATED_WEIGHT);
        assertThat(testPerson.getHeight()).isEqualTo(UPDATED_HEIGHT);
        assertThat(testPerson.getRunningCoefficient()).isEqualTo(UPDATED_RUNNING_COEFFICIENT);
        assertThat(testPerson.getPerformanceCoefficient()).isEqualTo(UPDATED_PERFORMANCE_COEFFICIENT);

        // Validate the Person in Elasticsearch
        verify(mockPersonSearchRepository, times(1)).save(testPerson);
    }

